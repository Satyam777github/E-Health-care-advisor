			Of those people who don't need diabetes medicine, some find that their diabetes does "reverse" with weight control, diabetes-healthy eating, and exercise. Their bodies are still able to make and use insulin, and their blood sugar levels go back to normal. Their diabetes is in remission.
			The first-line treatment for type 2 diabetes is diet, weight control and physical activity. If your blood sugar (glucose) level remains high despite a trial of these lifestyle measures then tablets to reduce the blood glucose level are usually advised. Insulin injections are needed in some cases if the blood glucose level remains too high despite taking tablets. Treatments for other related problems may also be advised.
